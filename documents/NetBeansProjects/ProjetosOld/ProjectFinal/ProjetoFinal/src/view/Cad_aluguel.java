    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        
        ArrayList dados= new ArrayList();
       String pesquisa=jTextField5.getText();
        conex.conexao();
        
        conex.executaSql("select id, nome, cpf, tel from cad_usuario where id="+pesquisa);
       
       try{
            
            conex.rs.first();
            
            do{
                
              dados.add(new Object[]{conex.rs.getInt("id"), conex.rs.getString("nome"), conex.rs.getString("cpf"), conex.rs.getString("tel")});
              String idtext=Integer.toString(conex.rs.getInt("id"));
              String nomet=conex.rs.getString("nome");
              String cpft=conex.rs.getString("cpf");
              String telt=conex.rs.getString("tel");
              jTextField5.setText(idtext);
              jTextField1.setText(nomet);
              jTextField6.setText(cpft);
              jTextField7.setText(telt);
            }while(conex.rs.next());
             
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Busque novamente!");
        }
              conex.desconecta();
              
           
           
    }//GEN-LAST:event_jButton6ActionPerformed

